package model;

/**
 *
 * @author Marek
 */
public class Cell {
    private int value;

    public Cell(int value) {
        this.value = value;
    } 
    
    public Cell() {
        this.value = 0;
    }

    public int getValue() {
        return value;
    }

    public void setValue(int value) {
        this.value = value;
    }

    @Override
    public String toString() {
        return String.valueOf(value);
    }
    
}
